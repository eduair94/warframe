#!/usr/bin/env node
/**
 * Repo map generator — writes docs/repo-map.md, a machine-generated index of the
 * codebase so an AI agent (or a human) can find the right file WITHOUT grepping
 * blind across 500+ frontend files and 40 huge i18n modules.
 *
 *   node scripts/gen-repo-index.mjs           # regenerate docs/repo-map.md
 *   node scripts/gen-repo-index.mjs --check    # fail (exit 1) if it is stale
 *
 * The --check form runs in CI (see .github/workflows/deploy.yml) so the map can
 * never silently rot. If it fails, run the generator and commit the result.
 *
 * Everything below is DERIVED from source — never hand-edit docs/repo-map.md.
 */
import { readFileSync, writeFileSync, readdirSync, statSync, existsSync } from 'node:fs';
import { join, relative, sep } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(fileURLToPath(new URL('.', import.meta.url)), '..');
const OUT = join(ROOT, 'docs', 'repo-map.md');

/* ------------------------------------------------------------------ helpers */

function walk(dir, filter, out = []) {
  if (!existsSync(dir)) return out;
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    const st = statSync(full);
    if (st.isDirectory()) walk(full, filter, out);
    else if (filter(full)) out.push(full);
  }
  return out;
}

const posix = (p) => p.split(sep).join('/');
const kb = (bytes) => `${Math.round(bytes / 1024)} KB`;

/* -------------------------------------------------------- API endpoints */
// Parse server.ts for `server.<method>('<path>', ...)`. The verb/flags come
// from the method name; the description is the block of `//` comment lines
// sitting directly above the registration.
function apiEndpoints() {
  const src = readFileSync(join(ROOT, 'server.ts'), 'utf8').split(/\r?\n/);
  const METHODS = {
    getJson: 'GET',
    getJsonCache: 'GET · cached',
    getJsonProtected: 'GET · protected',
    getJsonAuth: 'GET · auth',
    postJson: 'POST',
    postJsonAuth: 'POST · auth',
  };
  const rows = [];
  let comment = [];
  for (const line of src) {
    const t = line.trim();
    const c = t.match(/^\/\/\s?(.*)$/);
    if (c) {
      if (!/^---/.test(c[1])) comment.push(c[1]);
      continue;
    }
    const m = t.match(/^server\.(\w+)\(\s*['"]([^'"]+)['"]/);
    if (m && METHODS[m[1]]) {
      const path = m[2] === '/' ? '/' : `/${m[2]}`;
      rows.push({ path, verb: METHODS[m[1]], note: comment.join(' ').replace(/\s+/g, ' ').trim() });
    }
    comment = [];
  }
  return rows;
}

/* -------------------------------------------------------- frontend routes */
// Nuxt file-based routing: app/app/pages/**/*.vue -> URL path.
function fileToRoute(rel) {
  const parts = rel.replace(/\.vue$/, '').split('/');
  const segs = [];
  for (const p of parts) {
    if (p === 'index') continue;
    let s = p.replace(/^\[\[(.+)\]\]$/, ':$1?').replace(/^\[(.+)\]$/, ':$1');
    segs.push(s);
  }
  return '/' + segs.join('/');
}
function frontendRoutes() {
  const dir = join(ROOT, 'app', 'app', 'pages');
  return walk(dir, (f) => f.endsWith('.vue'))
    .map((f) => {
      const rel = posix(relative(dir, f));
      return { route: fileToRoute(rel) || '/', file: `app/app/pages/${rel}` };
    })
    .sort((a, b) => a.route.localeCompare(b.route));
}

/* -------------------------------------------------------- simple listings */
function listTs(dir, { excludeTest = true } = {}) {
  const abs = join(ROOT, dir);
  if (!existsSync(abs)) return [];
  return readdirSync(abs)
    .filter((n) => n.endsWith('.ts') && !(excludeTest && (n.endsWith('.test.ts') || n.endsWith('.spec.ts'))))
    .sort();
}
// Size is measured from CRLF-normalized content, NOT statSync — a Windows
// checkout stores these .ts files with CRLF (bigger on disk) while a Linux CI
// checkout uses LF. Raw byte size would then differ per-platform and make this
// map fail `--check` on CI. Normalized length is stable everywhere.
function i18nModules() {
  const dir = join(ROOT, 'app', 'i18n', 'messages');
  if (!existsSync(dir)) return [];
  return readdirSync(dir)
    .filter((n) => n.endsWith('.ts'))
    .map((n) => ({ name: n, size: Buffer.byteLength(readFileSync(join(dir, n), 'utf8').replace(/\r\n/g, '\n'), 'utf8') }))
    .sort((a, b) => b.size - a.size);
}

/* ------------------------------------------------------------------ render */
function render() {
  const endpoints = apiEndpoints();
  const routes = frontendRoutes();
  const services = listTs('services').filter((n) => !n.endsWith('.interface.ts'));
  const syncs = readdirSync(ROOT).filter((n) => /^sync_.*\.ts$/.test(n)).sort();
  const i18n = i18nModules();
  const bigI18n = i18n.filter((m) => m.size > 30 * 1024);

  const L = [];
  L.push('# Repo Map (auto-generated)');
  L.push('');
  L.push('> **Do not edit by hand.** Generated by `scripts/gen-repo-index.mjs` and');
  L.push('> verified in CI (`npm run repo:index:check`). Regenerate with `npm run repo:index`.');
  L.push('>');
  L.push('> Purpose: find the right file fast, without grepping. See `CLAUDE.md` for the');
  L.push('> narrative overview and per-directory `CLAUDE.md` files for local detail.');
  L.push('');

  L.push('## API endpoints');
  L.push('');
  L.push('Backend (`server.ts`), paths relative to the API root. Verbs/flags come from the');
  L.push('registration method (`getJsonCache` = cached, `*Auth` = Firebase-authed, `*Protected` = admin).');
  L.push('');
  L.push('| Path | Verb | What |');
  L.push('| --- | --- | --- |');
  for (const e of endpoints) {
    L.push(`| \`${e.path}\` | ${e.verb} | ${e.note || ''} |`);
  }
  L.push('');

  L.push('## Frontend routes → page file');
  L.push('');
  L.push('Nuxt 4 file-based routing under `app/app/pages/`. `:x?` = optional param.');
  L.push('');
  L.push('| Route | Page file |');
  L.push('| --- | --- |');
  for (const r of routes) {
    L.push(`| \`${r.route}\` | [${r.file}](${r.file}) |`);
  }
  L.push('');

  L.push('## Backend services (`services/`)');
  L.push('');
  L.push('Business logic + external clients. Each `*.ts` usually has a sibling `*.test.ts`.');
  L.push('');
  L.push(services.map((s) => `\`${s.replace(/\.ts$/, '')}\``).join(' · '));
  L.push('');

  L.push('## Sync jobs (root `sync_*.ts`)');
  L.push('');
  L.push('One-shot data importers run via `npm run <name>` / cron. They populate Mongo.');
  L.push('');
  L.push(syncs.map((s) => `\`${s}\``).join(' · '));
  L.push('');

  L.push('## i18n modules (`app/i18n/messages/`) — token traps');
  L.push('');
  L.push(`${i18n.length} locale modules. **Large ones — read narrowly (offset/limit or grep a key), never whole:**`);
  L.push('');
  L.push('| Module | Size |');
  L.push('| --- | --- |');
  for (const m of bigI18n) {
    L.push(`| \`app/i18n/messages/${m.name}\` | ${kb(m.size)} |`);
  }
  L.push('');
  L.push(`_${i18n.length - bigI18n.length} smaller modules omitted. See \`app/i18n/CLAUDE.md\`._`);
  L.push('');

  return L.join('\n');
}

/* ------------------------------------------------------------------ main */
const content = render();
const check = process.argv.includes('--check');
const current = existsSync(OUT) ? readFileSync(OUT, 'utf8') : '';

if (check) {
  // Compare line-ending-agnostic: a Windows checkout may store this file with
  // CRLF while the generator always emits LF. Only real content drift should fail.
  const norm = (s) => s.replace(/\r\n/g, '\n');
  if (norm(current) !== norm(content)) {
    console.error('✗ docs/repo-map.md is stale. Run `npm run repo:index` and commit the result.');
    process.exit(1);
  }
  console.log('✓ docs/repo-map.md is up to date.');
} else {
  writeFileSync(OUT, content);
  console.log(`✓ wrote docs/repo-map.md (${content.length} bytes)`);
}
